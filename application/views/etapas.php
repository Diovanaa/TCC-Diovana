      <h4><b>O que acontece com o sangue doado</b></h4> <br>
      Após a coleta, o sangue é separado em seus componentes dentro de 6 a 8 horas, no setor de processamento; <br>

      Cada componentes do sangue tem uma finalidade: <br>

      1- Glóbulos vermelhos – servem para tratar anemias;<br>
      2- Plasma (parte líquida do sangue) – serve para tratar hemorragias. Parte do plasma é encaminhado para indústrias que produzem fatores de coagulação, utilizados para tratar hemofílicos;<br>
      3 – Plaquetas – servem para tratar ou evitar sangramentos, e em pessoas que fazem quimioterapia.<br>
