    <h4><b>Processamento e Distribuição</b></h4> <br>
    Este setor realiza a separação do sangue em seus componentes para transfusão.<br>
    As bolsas coletadas são colocadas numa centrífuga que gira em uma determinada rotação por um período necessário para que o sangue seja fracionado, inicialmente em duas partes: a sólida e a líquida.<br>
    Os pacientes recebem somente a parte do sangue (hemocomponentes) que necessitam.<br>
    Alguns exemplos:<br><br>
    -As pessoas com grandes queimaduras precisam de Plasma;<br>
    -Os hemofílicos, pessoas que tem problemas de coagulação, necessitam do Fator VIII ou Crio Precipitado;<br>
    -Os portadores de anemias ou que tiveram hemorragias, recebem Concentrado de Hemácias;<br>
    -Os portadores de leucemia necessitam de Plaquetas.<br><br>
  <b>  Estoque de Hemocomponentes</b><br><br>

    Após a comprovação da qualidade do sangue, através dos exames sorológicos e imunohematológicos, os componentes do sangue são liberados para o estoque. No estoque, que localiza-se no Hemosc ou hospitais conveniados, os hemocomponentes são armazenados adequadamente, de acordo com a sua classificação e prazo de validade.<br><br>
    <b>Validade dos Hemocomponentes</b><br> <br>

    -Concentrado de Hemácias - de 28 a 35 dias a temperatura de 4 °;<br>
    -Concentrado de Plaquetas - 72 horas ou 5 dias a temperatura ambiente;<br>
    -Plasma - 12 meses e;<br>
    -Fator VIII ou Crio Precipitado - 12 meses. <br><br>
Simultaneamente ao processamento, amostras de sangue são encaminhadas aos laboratórios do hemocentro a fim de serem submetidas aos exames sorológicos e imunohematológicos. Os exames atestam a qualidade do sangue e classificam os tipos sangüíneos.<br><br>
 Quando se tratar de doação vinculada a um paciente, é necessário que o paciente tenha o cadastro no HEMOSC para possibilitar o vínculo da doação. Para cadastrar é necessário que ele mesmo, parentes ou pessoa autorizada, por telefone ou pessoalmente, faça o cadastro do paciente com as seguintes informações em mãos: Nº RG e órgão expedidor, CPF, nome completo, data nascimento, naturalidade, escolaridade, ocupação, e-mail, formação, nome pai, mãe e conjugê se houver, endereço residencial, fones para contato, hospital em que será ou está internado, tipo de procedimento a ser realizada(Cirurgia, transfusão...).

