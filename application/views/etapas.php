      <div class=" col-lg-8 jumbotron ">
        <h4><b>Etapas da doação</b></h4> <br>
        1º Passo – Recepção e cadastro <br><br>

        Apresentação de documento expedido por órgão oficial com foto (RG, CNH, Carteira de Trabalho, etc)<br>
        Atualização de endereço, trabalho, etc. <br><br>
        2° Passo – Pré-triagem <br><br>

        Peso, altura, verificação de pressão arterial, pulso e temperatura; <br>
        Uma "picadinha" no dedo para verificar seu hematócrito e/ ou hemoglobina; <br><br>
        3° Passo – Entrevista clínica <br><br>

        É confidencial e o sigilo é absoluto – confie em seu entrevistador e seja sincero;<br>
        Você assinará um Termo de Consentimento onde refere ter entendido as informações recebidas, ter respondido com sinceridade ao questionário, concorda com a coleta de sangue e afirma que está ciente de que se houver resultados sorológicos alterados, será chamado pelo serviço de apoio ao doador; <br> <br>
        4º Passo – Coleta de sangue <br><br>

        Será realizada a coleta de 450 ml ± 45ml e amostras de sangue para exames obrigatórios por lei; <br><br>
        5º Passo – Lanche <br> <br>

        É fornecido lanche acompanhado por líquidos, para repor o volume retirado na doação. Em casa ou no trabalho continue ingerindo bastante líquido. <br>
        Observe o que acontece com o sangue doado <br>
