    <h4><b>Não pode doar:</b> </h4> <br>
    Quem tem ou teve as seguintes doenças:<br><br>

    -Hepatite após os 11 anos de idade;<br>
    -Lepra (Hanseníase);<br>
    -Hipertireoidismo e tireoidite de Hashimoto;<br>
    -Doença auto-imune;<br>
    -Doença de Chagas;<br>
    -AIDS;<br>
    -Problemas cardíacos (necessita avaliação e declaração do seu cardiologista);<br>
    -Diabetes;<br>
    -Câncer;<br><br>
    Outras situações:<br><br>

    -Fez ou faz uso de algumas drogas ilícitas nos últimos 12 meses;<br>
    -Mantém relações sexuais de risco;<br>
    -Gestantes ou mulheres que amamentam bebês com menos de 12 meses;<br><br>
    Visando a prevenção de doenças causadas pelo Aedes aegypti, serão inaptos por 30 dias os candidatos à doação oriundos dos seguintes estados:<br><br>

    -Norte: Rondônia e Tocantins;<br>
    -Nordeste: Alagoas, Bahia, Ceará, Maranhão, Paraíba, Pernambuco e Rio Grande do Norte <br>

    -Centro-Oeste: Distrito Federal, Goiás, Mato Grosso e Mato Grosso do Sul <br>

    -Sudeste: Espírito Santo, Minas Gerais, Rio de Janeiro e São Paulo;<br>
    -Sul: Santa Catarina* e Paraná*, / Santa Catarina: Pinhalzinho, Serra Alta, Bom Jesus, Coronel Freitas, Descanso, Modelo União do Oeste e Chapecó. (Oriundos destes municípios poderão doar somente no Hemocentro Regional de Chapecó.) / Paraná*: Maringá, Londrina, Jataizinho, Marialva, Paranaguá, Cascavel e Cambé.<br>
    -Lembramos que estes estados são atualizados por boletins epidemiológicos semanalmente, podendo constar numa semana e não mais em outra, dúvidas entrar em contato através do fale conosco.<br>
