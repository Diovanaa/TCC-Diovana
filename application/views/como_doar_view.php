    <h4><b>Orientações preliminares ao doador de sangue:</b> </h4> <br>
    -Antes da doação você passará por um processo de pré - triagem (verificação de sinais vitais) e uma entrevista individual; <br>
    -Se você estiver com algum problema de saúde ou apresentando sintomas como perda de peso, manchas na pele, caroços pelo corpo (ínguas), feridas na boca, não doe sangue e procure um médico; <br>
    -Se o motivo que o levou a procurar nosso serviço é fazer exames, avise o profissional da entrevista. Você não deve doar sangue para esse fim. Basta procurar pelos Centro de Testagem e Aconselhamento (CTA) mais próximo, nestes locais os exames são gratuitos, os resultados são mais rápidos e não necessita solicitação do médico para realização: <br><br>
