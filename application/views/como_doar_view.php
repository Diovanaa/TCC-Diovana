  <div class="col-lg-8 jumbotron ">
    <h4><b>O que é necessário para doar? </b></h4> <br>
    -Ter idade entre 18 e 69 anos, 11 meses e 29 dias;</br>
    -Doadores com idade de 16 e 17 anos de idade, são aceitos para doação mediante a presença e autorização formal dos pais e/ou responsável legal; <br>
    -O limite de idade para primeira doação é de 60 anos;<br>
    -O candidato à doação deve estar em boas condições de saúde, sem feridas ou machucados no corpo;<br>
    -Pesar acima de 50 kg (com desconto de vestimentas);<br>
    -Apresentar documento de identidade com foto, emitido por órgão oficial: RG., carteira profissional, carteira de motorista, etc.<br>
    -Ter repousado bem na noite antes da doação;<br>
    -Evitar o jejum. Fazer refeições leves e não gordurosas, nas 4 horas que antecedem a doação;<br>
    -Evitar uso de bebidas alcoólicas nas últimas 12 horas;<br>
    -Evitar vir acompanhado com crianças, sem acompanhantes.<br>
  </div>
  <div class="col-lg-1">
