  -Se você for considerado apto para doar serão coletados cerca de 450ml do seu sangue, além da bolsa serão coletadas amostras de sangue para exames.<br>
  -Você realizará a higienização dos braços, com um produto chamado clorexidina, caso você seja alérgico, favor comunicar;<br>
  -O material utilizado para coleta é estéril e descartável, sendo assim, não existe risco nenhum de contrair doenças infecciosas na doação;<br>
  -Serão realizados exames para Hepatite B e C. Sífilis, Doença de Chagas, HIV, HTLV I e II, Tipagem sanguínea e fator RH, pesquisa de anticorpos irregulares.<br>
  -Existem, no entanto, doenças que podem ser transmitidas pelo sangue a quem for recebê-lo, para os quais não são feitos exames, por isso a importância de comunicar qualquer tipo de doença;<br>
  -Mesmo para os exames que são realizados, existe uma chance de dar resultado falso-negativo, ou seja a pessoa tem a doença, ou vírus, e o exame não consegue detectar por um período, logo após a pessoa contrair, chamado de janela imunológica. Por isso, a sinceridade do doador na entrevista é a maior segurança para quem vai receber o sangue;<br>
  -Caso apresente alguma alteração, você será chamado para confirmação de exames (via correio) e receberá orientações da equipe de apoio ao doador de nosso serviço; podem ocorrer resultados duvidosos (inconclusivos), sem que, necessariamente, signifiquem doenças, que serão posteriormente reanalisados;<br>
  -Os resultados positivos para HIV, Hepatite B e C, HTLV I e II, Sífilis e Chagas serão informados à Vigilância Epidemiológica;<br>
  -Você também poderá ser chamado se o paciente que receber o seu sangue apresentar algum problema;<br>
  -Geralmente os doadores não apresentam problemas durante a doação, mas algumas pessoas podem sentir-se mal. Neste caso, diante de qualquer anormalidade avise o profissional que o está atendendo que ele procederá os cuidados necessários para sua pronta recuperação;<br>
  -Apesar de todos os cuidados alguns doadores podem sentir tonturas durante o dia da doação e há o risco de surgir hematomas e dor no local da punção;<br>
  -Você tem o direito de desistir da doação caso não se sinta seguro e não deve doar se considerar que seu sangue pode causar algum problema para quem for recebê-lo;<br>
  -Esclareça todas as suas dúvidas durante a entrevista.

