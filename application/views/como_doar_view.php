  <div class="col-lg-8 jumbotron ">
    <h4><b>Após a Doação?</b> </h4> <br>
    -Não fumar por no mínimo duas horas;<br>
    -Nas 12 horas após a doação, não praticar exercícios físicos e atividades perigosas, como subir em locais altos ou dirigir caminhão, ônibus em rodovias, etc.<br>
    -Permanecer no serviço hemoterápico após a doação por 15 minutos;<br>
    -Não dobrar o braço em que foi realizada a punção no dia da doação, para evitar sangramentos e hematomas;<br>
    -Retirar o curativo 4 horas após a doação.<br>
